// 获取随机答案（防止连续两次相同）
let lastAnswerIndex = -1;

export function getRandomAnswer() {
  let newIndex;
  
  // 如果答案不止一条，防止连续出现相同答案
  do {
    newIndex = Math.floor(Math.random() * classicAnswers.length);
  } while (classicAnswers.length > 1 && newIndex === lastAnswerIndex);
  
  lastAnswerIndex = newIndex;
  return classicAnswers[newIndex];
}